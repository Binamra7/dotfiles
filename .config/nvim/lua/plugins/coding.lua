	{
		"mfussenegger/nvim-dap",
		dependencies = {
			"suketa/nvim-dap-ruby",
		},
		config = function()
			require("dap-ruby").setup()
		end,
	},
	{
		"olimorris/neotest-rspec",
	},
	{
		"sphamba/smear-cursor.nvim",
